import UIKit.UIFont

extension UIFont {

    static var naviTitle: UIFont { return UIFont(.montserrat(.Bold), 19) }
    
    static var title: UIFont { return UIFont(.montserrat(.Regular), 17) }
    
    static var text: UIFont { return UIFont(.montserrat(.Regular), 15) }
    
    static var summary: UIFont { return UIFont(.montserrat(.Light), 13) }
    
}
