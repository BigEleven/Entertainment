import UIKit

public extension CGFloat {
    
    /// 左间距
    static var left: CGFloat { return 30 }
    
    /// 右间距
    static var right: CGFloat { return -30 }
    
    /// 距离顶部
    static var toTop: CGFloat { return 15 }
    
    /// 模块间距
    static var spacing: CGFloat { return 10 }
}

